package Bean;

public class pici {
    private int pici;
    private String time;
    private int yiping;
    private int daiping;

    public int getPici() {
        return pici;
    }

    public void setPici(int pici) {
        this.pici = pici;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }


    public int getYiping() {
        return yiping;
    }

    public void setYiping(int yiping) {
        this.yiping = yiping;
    }

    public int getDaiping() {
        return daiping;
    }

    public void setDaiping(int daiping) {
        this.daiping = daiping;
    }
}
